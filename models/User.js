// Import mongoose
const mongoose = require('mongoose');
// Create Schema
const UserSchema = mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});
// Export user model schema
module.exports = mongoose.model('user', UserSchema);
